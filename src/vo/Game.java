package vo;

public class Game {
	private int gameNo;
	private String spaceTypeName;
	private String spaceNo;
	private int adminID;
	private String gameName;
	private String gameHost;
	private String gStartTime;
	private String gEndTime;
	private String gameHead;
	private String headPhone;
	
	public int getGameNo() {
		return gameNo;
	}
	public void setGameNo(int gameNo) {
		this.gameNo = gameNo;
	}
	public String getSpaceTypeName() {
		return spaceTypeName;
	}
	public void setSpaceTypeName(String spaceTypeName) {
		this.spaceTypeName = spaceTypeName;
	}
	public String getSpaceNo() {
		return spaceNo;
	}
	public void setSpaceNo(String spaceNo) {
		this.spaceNo = spaceNo;
	}
	public int getAdminID() {
		return adminID;
	}
	public void setAdminID(int adminID) {
		this.adminID = adminID;
	}
	public String getGameName() {
		return gameName;
	}
	public void setGameName(String gameName) {
		this.gameName = gameName;
	}
	public String getGameHost() {
		return gameHost;
	}
	public void setGameHost(String gameHost) {
		this.gameHost = gameHost;
	}
	public String getgStartTime() {
		return gStartTime;
	}
	public void setgStartTime(String gStartTime) {
		this.gStartTime = gStartTime;
	}
	public String getgEndTime() {
		return gEndTime;
	}
	public void setgEndTime(String gEndTime) {
		this.gEndTime = gEndTime;
	}
	public String getGameHead() {
		return gameHead;
	}
	public void setGameHead(String gameHead) {
		this.gameHead = gameHead;
	}
	public String getHeadPhone() {
		return headPhone;
	}
	public void setHeadPhone(String headPhone) {
		this.headPhone = headPhone;
	}
	
	
}
