package vo;

public class SetFines {

	private int finesNo;
	private String finesTittle;
	private String finesDetail;
	
	public int getFinesNo() {
		return finesNo;
	}
	public void setFinesNo(int finesNo) {
		this.finesNo = finesNo;
	}
	public String getFinesTittle() {
		return finesTittle;
	}
	public void setFinesTittle(String finesTittle) {
		this.finesTittle = finesTittle;
	}
	public String getFinesDetail() {
		return finesDetail;
	}
	public void setFinesDetail(String finesDetail) {
		this.finesDetail = finesDetail;
	}
	
}
