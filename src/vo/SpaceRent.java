package vo;

public class SpaceRent {
    
	private String spaceTypeName;
    private String spaceNo;
    private String sStartTime;
	private String sEndTime;
	private String sRentPeople;
	
	public String getspaceTypeName() {
		return spaceTypeName;
	}
	public void setspaceTypeName(String spaceTypeName) {
		this.spaceTypeName = spaceTypeName;
	}
	public String getSpaceNo() {
		return spaceNo;
	}
	public void setSpaceNo(String spaceNo) {
		this.spaceNo = spaceNo;
	}
	public String getsStartTime() {
		return sStartTime;
	}
	public void setsStartTime(String sStartTime) {
		this.sStartTime = sStartTime;
	}
	public String getsEndTime() {
		return sEndTime;
	}
	public void setsEndTime(String sEndTime) {
		this.sEndTime = sEndTime;
	}
	public String getsRentPeople() {
		return sRentPeople;
	}
	public void setsRentPeople(String sRentPeople) {
		this.sRentPeople = sRentPeople;
	}
	
	
}
